/**
 * Базовые типы для n-мерной геометрии.
 *
 * Соглашения:
 * - Вектор в ℝⁿ — это просто Float64Array длины n.
 *   Float64Array вместо number[] для скорости и стабильности при
 *   рекуррентных операциях (Грам-Шмидт, повороты Гивенса).
 * - Матрица n×n — Float64Array длины n² в row-major порядке:
 *   M[i, j] = data[i*n + j].
 * - Вершина гиперкуба идентифицируется целым числом v ∈ [0, 2ⁿ):
 *   бит i числа v задаёт знак i-й координаты:
 *     bit = 0 → координата = -1
 *     bit = 1 → координата = +1
 *   То есть вершины — это в точности множество {-1, +1}ⁿ, центрированное в нуле.
 */

export type NDVector = Float64Array;
export type NDMatrix = Float64Array;

/**
 * Индекс вершины — целое в [0, 2ⁿ).
 * Битовое представление = координаты в базисе {-1, +1}.
 */
export type VertexId = number;

/**
 * Ребро гиперкуба.
 * - `a`, `b` — индексы концевых вершин, a < b.
 * - `axis` ∈ [0, n) — индекс координаты, по которой вершины различаются
 *   (a ⊕ b имеет единственный установленный бит axis).
 * Это инвариант: рёбра 1-в-1 соответствуют парам (a, axis) с битом axis = 0 в a.
 */
export interface Edge {
  readonly a: VertexId;
  readonly b: VertexId;
  readonly axis: number;
}

/**
 * k-грань гиперкуба — это аффинный объект, заданный:
 * - `freeMask`: битовая маска подмножества {0..n-1} мощности k —
 *   индексы «свободных» координат, изменяющихся по грани.
 * - `fixedValues`: битовая маска (n - k) фиксированных координат:
 *   бит i имеет смысл только если бит i в freeMask = 0; значение бита
 *   задаёт знак (0 → -1, 1 → +1).
 *
 * Количество k-граней в n-кубе: C(n, k) · 2^(n-k).
 *
 * Пример: в 4-кубе грань {x₁ = +1, x₂ = -1, x₃ свободна, x₄ свободна}:
 *   freeMask    = 0b1100 (биты 2 и 3 свободны)
 *   fixedValues = 0b0001 (бит 0 = 1 → x₁ = +1; бит 1 = 0 → x₂ = -1;
 *                         биты 2,3 в fixedValues игнорируются)
 */
export interface Face {
  readonly freeMask: number;
  readonly fixedValues: number;
  readonly dim: number;
}

/**
 * Результат проекции одной вершины.
 *
 * - `pos`: координаты в R³ (только если !clipped).
 * - `clipped`: точка попала за «горизонт событий» на каком-то уровне
 *   проекции (w_i ≤ -d_i, то есть позади камеры).
 * - `hiddenDepths`: список значений «глубоких» координат, отброшенных
 *   на каждом уровне. Длина = n - 3. Используется для:
 *     - spectral z-buffer (порядок отрисовки),
 *     - визуализации «event horizon» (близость к сингулярности),
 *     - окраски по высшим измерениям.
 * - `minMargin`: min_i (d_i + w_i) — расстояние до ближайшей
 *   сингулярной гиперплоскости. Маленький margin → красное предупреждение.
 */
export interface ProjectionResult {
  readonly pos: [number, number, number];
  readonly clipped: boolean;
  readonly hiddenDepths: number[];
  readonly minMargin: number;
}
